package java_concept;

public class LoopAssignment {

	public static void main(String[] args) {
		
				
				
				for(int m=0; m<=4; m++) {
					for(int n=0; n<=4; n++) {
						for(int r=0; r<=4;r++) {
							
							System.out.print(m+""+n+ "" +r +" ");//000 001 002 003 004
						}
						System.out.println();
					}
					
				}
	}

}
